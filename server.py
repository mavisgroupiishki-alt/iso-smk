def _pdf_pages_to_images(file_bytes, max_pages=3, max_dim=1900, quality=82):
    """
    Конвертирует страницы PDF в настоящие JPEG-картинки перед отправкой в vision.
    КРИТИЧНО: раньше PDF отправлялся в vision как есть (media_type=application/pdf
    внутри image_url) — модель отвечала ошибкой 'unsupported_format', потому что
    image_url ожидает именно изображение, не документ. PDF-сканы (паспорт, диплом,
    трудовая) из-за этого не распознавались НИКОГДА, даже когда доходили до vision.

    Используем pypdfium2 (не pdf2image/Poppler — Render Free блокирует apt-get на
    этапе сборки, файловая система только для чтения; не PyMuPDF — не удалось
    подтвердить его установку живым тестом в песочнице разработки). pypdfium2 —
    Python-обёртка над PDFium (движок PDF из Chromium), собранная как самодостаточное
    колесо без единой системной зависимости — проверено вживую перед отправкой этого
    кода: реальный PDF отрендерен и превращён в JPEG успешно.

    Берём только первые max_pages страниц — этого достаточно для типичных сканов
    документов (диплом/паспорт/страница трудовой — обычно 1 разворот на файл).
    """
    import pypdfium2 as pdfium
    from PIL import Image
    import io as _io4
    images_b64 = []
    doc = pdfium.PdfDocument(file_bytes)
    try:
        n_pages = min(len(doc), max_pages)
        for i in range(n_pages):
            page = doc[i]
            bitmap = page.render(scale=150/72)  # 150 DPI
            img = bitmap.to_pil().convert('RGB')
            if max(img.size) > max_dim:
                ratio = max_dim / max(img.size)
                img = img.resize((int(img.size[0]*ratio), int(img.size[1]*ratio)), Image.LANCZOS)
            buf = _io4.BytesIO()
            img.save(buf, 'JPEG', quality=quality, optimize=True)
            images_b64.append(base64.b64encode(buf.getvalue()).decode('utf-8'))
    finally:
        doc.close()
    return images_b64


TESSDATA_DIR = BASE_DIR / 'tessdata'
_TESSERACT_STATUS = {'checked': False, 'available': False, 'has_rus': False, 'reason': ''}

def _check_tesseract():
    """Проверяет один раз при первом обращении: стоит ли вообще tesseract на сервере
    и есть ли русский языковой пакет (из репозитория, папка tessdata/ — не системный,
    чтобы не зависеть от apt-get, который Render блокирует на этапе сборки)."""
    if _TESSERACT_STATUS['checked']:
        return _TESSERACT_STATUS
    _TESSERACT_STATUS['checked'] = True
    try:
        import pytesseract
        pytesseract.get_tesseract_version()
        _TESSERACT_STATUS['available'] = True
    except Exception as e:
        _TESSERACT_STATUS['reason'] = f'бинарник tesseract недоступен: {e}'
        print(f"  ℹ️ Tesseract OCR недоступен ({_TESSERACT_STATUS['reason']}) — все файлы пойдут через vision как раньше")
        return _TESSERACT_STATUS
    rus_path = TESSDATA_DIR / 'rus.traineddata'
    if rus_path.exists():
        _TESSERACT_STATUS['has_rus'] = True
        print(f"  ✅ Tesseract OCR доступен, русский языковой пакет найден в {rus_path}")
    else:
        _TESSERACT_STATUS['reason'] = f'нет файла {rus_path} (нужно положить в репозиторий, папка tessdata/)'
        print(f"  ℹ️ Tesseract доступен, но {_TESSERACT_STATUS['reason']} — все файлы пойдут через vision")
    return _TESSERACT_STATUS


def _tesseract_ocr_image(pil_image):
    """OCR одной картинки локальным Tesseract (без похода в интернет вообще).
    Возвращает текст или None, если результат выглядит ненадёжным (мало текста,
    много не-буквенного мусора — типичный признак что Tesseract не справился, например
    с рукописным текстом или сильно смазанным фото)."""
    import pytesseract
    import os as _os
    _os.environ['TESSDATA_PREFIX'] = str(TESSDATA_DIR)
    text = pytesseract.image_to_string(pil_image, lang='rus+eng', config='--tessdata-dir "%s"' % TESSDATA_DIR)
    text = text.strip()
    if len(text) < 15:
        return None  # почти ничего не нашёл — вероятно скан плохого качества или рукопись
    if not _looks_like_real_text(text):
        return None  # похоже на мусор, а не реальный текст
    return text


def _try_tesseract_first(file_bytes, filename):
    """Пытается прочитать файл локальным OCR ПЕРЕД тем как идти во внешний vision API.
    Работает только для ПЕЧАТНОГО текста (дипломы/справки/официальные бланки — почти
    всегда печать) — для рукописного текста (старые записи в трудовой от руки) заведомо
    хуже ИИ-vision, поэтому в таких случаях намеренно возвращает None, чтобы код упал
    на vision как раньше. Возвращает текст или None (None = 'иди в vision')."""
    status = _check_tesseract()
    if not (status['available'] and status['has_rus']):
        return None
    try:
        from PIL import Image
        import io as _io5
        ext = filename.rsplit('.', 1)[-1].lower() if '.' in filename else ''
        if ext == 'pdf':
            pages_b64 = _pdf_pages_to_images(file_bytes, max_pages=3)
            texts = []
            for b64 in pages_b64:
                img = Image.open(_io5.BytesIO(base64.b64decode(b64)))
                t = _tesseract_ocr_image(img)
                if t:
                    texts.append(t)
            if not texts:
                return None
            return '\n\n'.join(texts)
        else:
            small_bytes, _ = _downscale_image(file_bytes, max_dim=2400, quality=90)  # для OCR чуть покрупнее чем для vision
            img = Image.open(_io5.BytesIO(small_bytes))
            return _tesseract_ocr_image(img)
    except Exception as e:
        print(f"  ⚠️ Tesseract OCR упал с ошибкой на {filename} ({type(e).__name__}: {e}) — иду в vision")
        return None


def vision_extract(file_bytes, filename, api_key, media_type=None, prompt_override=None):
    """Синхронный вызов vision для одного файла (фото/скан). Сначала пробует локальный
    Tesseract OCR (бесплатно, быстро, не зависит от внешнего API) — если он недоступен
    на сервере или не справился (плохой скан/рукопись), падает на внешний vision API
    как раньше. Уменьшает изображение перед отправкой. Ограничивает параллелизм через
    VISION_SEMAPHORE (не более 2 разом на весь сервер — иначе Render Free падает по памяти)."""
    import base64 as _b64, time as _time
    active_prompt = prompt_override or VISION_PROMPT
    ext = filename.rsplit('.', 1)[-1].lower() if '.' in filename else ''

    if ext in ('jpg', 'jpeg', 'png', 'webp', 'heic', 'heif', 'pdf'):
        tesseract_text = _try_tesseract_first(file_bytes, filename)
        if tesseract_text:
            print(f"  ✅ vision_extract({filename}): прочитано локальным Tesseract OCR, "
                  f"{len(tesseract_text)} символов — в vision API не ходили")
            return tesseract_text

    if ext == 'pdf':
        # PDF конвертируется в настоящие картинки — см. _pdf_pages_to_images().
        try:
            pages_b64 = _pdf_pages_to_images(file_bytes)
        except Exception as e:
            print(f"  ❌ vision_extract({filename}): не удалось конвертировать PDF в изображения — {type(e).__name__}: {e}")
            return f'[PDF: не удалось подготовить для распознавания — {e}]'
        if not pages_b64:
            return '[PDF: страницы не найдены]'
        content_blocks = [{"type": "image_url", "image_url": {"url": f"data:image/jpeg;base64,{b64}"}}
                           for b64 in pages_b64]
        content_blocks.append({"type": "text", "text": active_prompt +
                                (f"\n\n(Документ из {len(pages_b64)} страниц — учти все страницы вместе.)"
                                 if len(pages_b64) > 1 else '')})
        payload_mb = sum(len(b) for b in pages_b64) / 1024 / 1024
        vibe_payload = {"model": VIBE_MODEL_VISION, "max_tokens": 8000,
                         "messages": [{"role": "user", "content": content_blocks}]}
        print(f"  🔎 vision_extract({filename}): PDF -> {len(pages_b64)} стр., отправляю {payload_mb:.2f} МБ, жду семафор...")
        VISION_SEMAPHORE.acquire()
        t0 = _time.time()
        try:
            resp = req_lib.post(VIBE_URL, headers={"Content-Type": "application/json", "X-Api-Key": api_key},
                                 json=vibe_payload, timeout=90)
            elapsed = _time.time() - t0
            resp.raise_for_status()
            data = resp.json()
            text = "".join((c.get("message", {}).get("content") or "") for c in data.get("choices", []))
            print(f"  ✅ vision_extract({filename}): успех за {elapsed:.1f} сек, {len(text)} символов ответа")
            if not text:
                print(f"  ⚠️ vision_extract({filename}): ПУСТОЙ content — сырой ответ API (первые 500 симв): {json.dumps(data)[:500]}")
            return text or '[vision: пустой ответ]'
        except req_lib.exceptions.Timeout:
            elapsed = _time.time() - t0
            print(f"  ⏱️ vision_extract({filename}): ТАЙМАУТ через {elapsed:.1f} сек")
            return f'[vision: таймаут при распознавании {filename}]'
        except Exception as e:
            elapsed = _time.time() - t0
            print(f"  ❌ vision_extract({filename}): ОШИБКА через {elapsed:.1f} сек — {type(e).__name__}: {e}")
            return f'[vision ошибка: {e}]'
        finally:
            VISION_SEMAPHORE.release()

    if ext in ('jpg', 'jpeg', 'png', 'webp', 'heic', 'heif'):
        small_bytes, mt = _downscale_image(file_bytes)
        file_bytes = small_bytes
        media_type = mt or (media_type or 'image/jpeg')
    elif not media_type:
        media_type = 'image/jpeg'

    b64_data = _b64.b64encode(file_bytes).decode('utf-8')
    payload_mb = len(b64_data) / 1024 / 1024
    vibe_payload = {
        "model": VIBE_MODEL_VISION,
        "max_tokens": 8000,
        "messages": [{
            "role": "user",
            "content": [
                {"type": "image_url", "image_url": {"url": f"data:{media_type};base64,{b64_data}"}},
                {"type": "text", "text": active_prompt}
            ]
        }]
    }
    print(f"  🔎 vision_extract({filename}): отправляю {payload_mb:.2f} МБ (base64), жду семафор...")
    VISION_SEMAPHORE.acquire()
    t0 = _time.time()
    try:
        resp = req_lib.post(
            VIBE_URL, headers={"Content-Type": "application/json", "X-Api-Key": api_key},
            json=vibe_payload, timeout=90
        )
        elapsed = _time.time() - t0
        resp.raise_for_status()
        data = resp.json()
        text = "".join((c.get("message", {}).get("content") or "") for c in data.get("choices", []))
        print(f"  ✅ vision_extract({filename}): успех за {elapsed:.1f} сек, {len(text)} символов ответа")
        if not text:
            print(f"  ⚠️ vision_extract({filename}): ПУСТОЙ content — сырой ответ API (первые 500 симв): {json.dumps(data)[:500]}")
        return text or '[vision: пустой ответ]'
    except req_lib.exceptions.Timeout:
        elapsed = _time.time() - t0
        print(f"  ⏱️ vision_extract({filename}): ТАЙМАУТ через {elapsed:.1f} сек (лимит 90с) — файл {payload_mb:.2f} МБ")
        return f'[vision: таймаут после {elapsed:.0f} сек — файл {payload_mb:.1f} МБ, возможно слишком большой или сервис перегружен]'
    except Exception as e:
        elapsed = _time.time() - t0
        print(f"  ❌ vision_extract({filename}): ОШИБКА через {elapsed:.1f} сек — {type(e).__name__}: {e}")
        return f'[vision ошибка: {type(e).__name__}: {e}]'
    finally:
        VISION_SEMAPHORE.release()


# ── Извлечение текста из файлов (рекурсивно) ────────────────
def _looks_like_real_text(s: str) -> bool:
    """
    Отличает настоящий читаемый текст от мусора, который может получиться при
    попытке regex-разбора СЖАТОГО потока PDF (байты сжатых данных иногда случайно
    складываются в нечто похожее на "BT...(текст)...ET", но это не текст).
    Настоящий текст (даже на русском/белорусском) почти целиком состоит из букв,
    цифр, пробелов и обычной пунктуации. Мусор из сжатых данных — нет.
    """
    if not s or len(s.strip()) < 10:
        return False
    allowed = set(' \t\n\r.,;:!?()-–—«»"\'/%№0123456789'
                  'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ'
                  'абвгдеёжзийклмнопрстуфхцчшщъыьэюяАБВГДЕЁЖЗИЙКЛМНОПРСТУФХЦЧШЩЪЫЬЭЮЯ'
                  'іўІЎ')  # белорусские буквы
    good = sum(1 for c in s if c in allowed)
    ratio = good / len(s)
    return ratio >= 0.85


def extract_text_from_file(file_bytes, filename, _depth=0):
    """Рекурсивно читает файлы и архивы внутри архивов (до 3 уровней)"""
    if _depth > 3:
        return '[слишком глубокая вложенность архивов]'

    ext = filename.rsplit('.', 1)[-1].lower() if '.' in filename else ''
    try:
        if ext in ('txt', 'csv'):
            return file_bytes.decode('utf-8', errors='replace')[:8000]

        elif ext == 'docx' or ext == 'doc':
            import io
            try:
                with zipfile.ZipFile(io.BytesIO(file_bytes)) as z:
                    if 'word/document.xml' in z.namelist():
                        xml = z.read('word/document.xml').decode('utf-8', errors='replace')
                        text = re.sub(r'<[^>]+>', ' ', xml)
                        text = re.sub(r'\s+', ' ', text).strip()
                        return text[:8000]
            except: pass
            return '[docx: не удалось прочитать]'

        elif ext == 'pdf':
            # BT/ET поток парсер
            # ВАЖНО: сканируем только начало файла, а не весь файл целиком.
            # Большие PDF (десятки МБ) почти всегда — сканы без текстового слоя (BT/ET там нет вообще),
            # а decode()+regex по всем байтам такого файла — верный способ упасть в OOM на Render Free.
            #
            # КРИТИЧНЫЙ БАГ (найден и исправлен): если содержимое PDF сжато (FlateDecode —
            # это подавляющее большинство современных PDF и сканов), то regex BT...ET иногда
            # СЛУЧАЙНО находит похожие последовательности байт внутри сжатого мусора и вытаскивает
            # бессмысленный "текст". Раньше это принималось как успешное извлечение (длиннее 50
            # символов, не начинается с "[") — и код НИКОГДА не переходил к vision, хотя должен
            # был. Именно так терялись реальные данные людей (дипломы/паспорта/трудовые сканы) —
            # функция "успешно" возвращала мусор вместо текста, vision не вызывался вообще.
            PDF_SCAN_LIMIT = 800 * 1024  # 800 КБ хватает с запасом для обычных текстовых PDF
            try:
                raw = file_bytes[:PDF_SCAN_LIMIT].decode('latin-1', errors='replace')
                blocks = re.findall(r'BT(.*?)ET', raw, re.DOTALL)
                result = []
                for b in blocks:
                    strings = re.findall(r'\(([^)]{2,})\)', b)
                    result.extend(strings)
                if result:
                    candidate = ' '.join(result)[:8000]
                    if _looks_like_real_text(candidate):
                        return candidate
                    # похоже на мусор из сжатого потока — не принимаем как успех,
                    # падаем в [PDF_SCAN: файл является сканом] ниже -> вызовет vision
            except Exception:
                pass
            return '[PDF_SCAN: файл является сканом]'

        elif ext in ('xlsx', 'xls'):
            import io, re as _re2
            # Сначала пробуем как XLSX (ZIP-формат)
            try:
                with zipfile.ZipFile(io.BytesIO(file_bytes)) as z:
                    shared = []
                    if 'xl/sharedStrings.xml' in z.namelist():
                        xml = z.read('xl/sharedStrings.xml').decode('utf-8', errors='replace')
                        shared = re.findall(r'<t[^>]*>([^<]+)</t>', xml)
                    sheets = [n for n in z.namelist() if n.startswith('xl/worksheets/sheet')]
                    if sheets:
                        xml = z.read(sheets[0]).decode('utf-8', errors='replace')
                        refs = re.findall(r'<v>(\d+)</v>', xml)
                        values = [shared[int(r)] for r in refs if int(r) < len(shared)]
                        if values:
                            return ' | '.join(values[:300])
                return '[xlsx: данные не найдены]'
            except zipfile.BadZipFile:
                pass
            except Exception:
                pass
            # Старый XLS формат — эвристика
            try:
                text_utf16 = file_bytes.decode('utf-16-le', errors='ignore')
                chunks = _re2.findall(r'[\u0400-\u04ff\w][\u0400-\u04ff\w\s\.\,\-]{3,}', text_utf16)
                if len(chunks) > 3:
                    return ' | '.join(c.strip() for c in chunks if len(c.strip()) > 3)[:6000]
            except Exception:
                pass
            try:
                raw = file_bytes.decode('cp1251', errors='ignore')
                lines = [l.strip() for l in raw.split('\n') if len(l.strip()) > 5]
                readable = [l for l in lines if any('а' <= c <= 'я' or 'А' <= c <= 'Я' for c in l)]
                if readable:
                    return '\n'.join(readable[:200])
            except Exception:
                pass
            return '[XLS: не удалось прочитать. Пересохраните файл как .xlsx]'

        elif ext in ('zip',):
            return _extract_archive_zip(file_bytes, filename, _depth)

        elif ext == 'rar':
            return _extract_archive_rar(file_bytes, filename, _depth)

    except Exception as e:
        return f'[Ошибка чтения {filename}: {e}]'

    return '[Неизвестный формат файла]'


def _simple_ai_call(prompt, api_key, max_tokens=1500):
    """Минимальный вызов текстовой модели БЕЗ системного промпта Игоря (AI_SYSTEM
    требует отвечать строго JSON — для задач вроде сведения карточки человека это
    не подходит и всё сломает). Отдельная функция специально для таких служебных
    задач — не путать с call_ai (это для основного чата с Игорем)."""
    resp = req_lib.post(
        VIBE_URL,
        headers={"Content-Type": "application/json", "X-Api-Key": api_key},
        json={"model": VIBE_MODEL, "max_tokens": max_tokens,
              "messages": [{"role": "user", "content": prompt}]},
        timeout=90
    )
    resp.raise_for_status()
    data = resp.json()
    text = "".join((c.get("message", {}).get("content") or "") for c in data.get("choices", []))
    if not text:
        raise RuntimeError(f"Пустой ответ от модели при сведении данных (сырой ответ: {json.dumps(data)[:300]})")
    return text


def _group_blocks_by_person(texts):
    """Группирует блоки текста '--- путь/файл ---\\nсодержимое' по папке (папка = человек,
    та же логика что и в диагностической кнопке на фронтенде). Возвращает (groups, order):
    groups — {имя_папки_или_None: [блок, блок, ...]}, order — порядок первого появления."""
    groups = {}
    order = []
    for block in texts:
        m = re.match(r'^--- (.+?) ---', block)
        if not m:
            groups.setdefault(None, []).append(block)
            if None not in order: order.append(None)
            continue
        full_path = m.group(1).strip()
        slash_idx = full_path.rfind('/')
        person = full_path[:slash_idx] if slash_idx >= 0 else None
        groups.setdefault(person, []).append(block)
        if person not in order: order.append(person)
    return groups, order


